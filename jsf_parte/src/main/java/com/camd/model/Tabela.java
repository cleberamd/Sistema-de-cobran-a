package com.camd.model;

public class Tabela {
	
	private String codcfo;
	private String nome;
	private int qtdparc;
	private double valorp;
	
	
	public String getCodcfo() {
		return codcfo;
	}
	public void setCodcfo(String codcfo) {
		this.codcfo = codcfo;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getQtdparc() {
		return qtdparc;
	}
	public void setQtdparc(int qtdparc) {
		this.qtdparc = qtdparc;
	}
	public double getValorp() {
		return valorp;
	}
	public void setValorp(double valorp) {
		this.valorp = valorp;
	}
	
	

}
